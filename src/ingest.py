    try:
        cleaned_master = pipeline_data_alignment(
            hub_path=HUB_FILE,
            local_path=LOCAL_FILE,
            hub_name="azadpur",
            local_name="imphal"
        )

        # Save structural target frame
        output_csv = os.path.join(PROCESSED_DIR, "aligned_market_returns.csv")
        cleaned_master.to_csv(output_csv)
        print(f"[+] Clean unified dataset written to: {output_csv}")

        # Generate Figure 1
        generate_diagnostic_plots(cleaned_master, "price_azadpur", "price_imphal", FIG_DIR)

    except FileNotFoundError as e:
        print(f"[x] Pipeline stopped: Ensure your raw data downloads exist in '{RAW_DIR}/'.")
        print(f"    Detail: {e}")
